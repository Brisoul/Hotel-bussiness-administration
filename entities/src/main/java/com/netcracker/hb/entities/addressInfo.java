package com.netcracker.hb.entities;

public class addressInfo {

    private String countryName;
    private String townName;
    private String streetName;
    private int houseNum;
    private int entranceNum;
    private int floorNum;
    private int floatNum;


    public void setCountryName(String countryName) {
        this.countryName = countryName;
    }

    public String getCountryName() {
        return countryName;
    }

    public void setTownName(String townName) {
        this.townName = townName;
    }

    public String getTownName() {
        return townName;
    }

    public void setStreetName(String streetName) {
        this.streetName = streetName;
    }

    public String getStreetName() {
        return streetName;
    }

    public void setHouseNum(int houseNum) {
        this.houseNum = houseNum;
    }

    public int getHouseNum() {
        return houseNum;
    }

    public void setEntranceNum(int entranceNum) {
        this.entranceNum = entranceNum;
    }

    public int getEntranceNum() {
        return entranceNum;
    }

    public void setFloorNum(int floorNum) {
        this.floorNum = floorNum;
    }

    public int getFloorNum() {
        return floorNum;
    }

    public void setFloatNum(int floatNum) {
        this.floatNum = floatNum;
    }

    public int getFloatNum() {
        return floatNum;
    }

}
